        [Test]
        public void ConvertOrderByteToOrderIndexes_should_work()
        {
            uint orderByte = 0x01234567;
            var indexes = StencilPainter.ConvertOrderByteToOrderIndexes(orderByte);
            Assert.That(indexes, Is.EquivalentTo(new[] { 0, 1, 2, 3, 4, 5, 6, 7 }));


        }

        [Test]
        public void OrderStencilFrames_should_work()
        {
            var stencilFrames = new StencilFrame[8];
            for (var i = 0; i < stencilFrames.Length; i += 1)
            {
                stencilFrames[i] = new StencilFrame(IntPtr.Zero, IntPtr.Zero, IntPtr.Zero, IntPtr.Zero,
                    (byte)i, 0, 0);
            }

            var orderedStencilFrames = StencilPainter.OrderStencilFrames8(stencilFrames, new uint[] { 0, 1, 2, 3, 4, 5, 6, 7 });

            Assert.That(orderedStencilFrames.Select(frame => frame.MaskTree), Is.EquivalentTo(new[] { 0, 1, 2, 3, 4, 5, 6, 7 }));

            orderedStencilFrames = StencilPainter.OrderStencilFrames8(stencilFrames, new uint[] { 2, 6, 4 });

            Assert.That(orderedStencilFrames.Select(frame => frame.MaskTree), Is.EquivalentTo(new[] { 2, 6, 4 }));
        }

        private byte ConvertMaskTreeBytes(byte[] maskTreeBytes)
        {
            byte maskTree = 0;
            for (var i = 0; i < 8; i += 1)
            {
                maskTree += (byte)(maskTreeBytes[i] << i);
            }

            return maskTree;
        }

        private StencilFrame CreateStencilFrame(byte[] yBytes, byte[] uBytes, byte[] vBytes, byte[] aBytes, byte[] maskTreeBytes, int width, int height)
        {
            var maskTree = ConvertMaskTreeBytes(maskTreeBytes);
            var yPtr = Marshal.UnsafeAddrOfPinnedArrayElement(yBytes, 0);
            var uPtr = Marshal.UnsafeAddrOfPinnedArrayElement(uBytes, 0);
            var vPtr = Marshal.UnsafeAddrOfPinnedArrayElement(vBytes, 0);
            var aPtr = Marshal.UnsafeAddrOfPinnedArrayElement(aBytes, 0);

            return new StencilFrame(yPtr, uPtr,
                vPtr, aPtr, maskTree, width, height);
        }

        private unsafe Yuv444Frame SimplePaintYuv444(int width, int height, StencilFrame[] stencilFrames)
        {
            var yuv444Frame = new Yuv444Frame(width, height);
            foreach (var stencilFrame in stencilFrames)
            {
                var yPtr = (byte*)stencilFrame.YPtr.ToPointer();
                var uPtr = (byte*)stencilFrame.UPtr.ToPointer();
                var vPtr = (byte*)stencilFrame.VPtr.ToPointer();
                var alphaPtr = (byte*)stencilFrame.AlphaPtr.ToPointer();

                for (var y = 0; y < height; y += 1)
                {
                    for (var x = 0; x < width; x += 1)
                    {
                        var pixelIndex = y * width + x;
                        if (alphaPtr[pixelIndex] <= 0 || yuv444Frame.ColoredBytes[pixelIndex])
                        {
                            continue;
                        }

                        var stencilChromaIndex = (stencilFrame.Width / 2) * (y / 2) + (x / 2);

                        yuv444Frame.YBytes[pixelIndex] = yPtr[pixelIndex];
                        yuv444Frame.UBytes[pixelIndex] = uPtr[stencilChromaIndex];
                        yuv444Frame.VBytes[pixelIndex] = vPtr[stencilChromaIndex];
                        yuv444Frame.ColoredBytes[pixelIndex] = true;
                    }
                }
            }

            return yuv444Frame;
        }


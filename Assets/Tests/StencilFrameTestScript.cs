                0x00, 0xFF, 0x00, 0x00,
                0x00, 0x00, 0x00, 0xFF };
            maskTreeBytesList[2] = new byte[] {
                0, 1, 0, 0,
                0, 0, 0, 1 };

            var resultY = new byte[] {
                0x01, 0x18, 0x03, 0x00,
                0x05, 0x06, 0x23, 0x40 };
            var resultU = new byte[] {
                0x09, 0x25, 0x10, 0x00,
                0x09, 0x09, 0x26, 0x42 };
            var resultV = new byte[] {
                0x13, 0x29, 0x14, 0x00,
                0x13, 0x13, 0x30, 0x46 };


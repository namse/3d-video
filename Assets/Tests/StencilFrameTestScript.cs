            Assert.That(yuv444Frame.UBytes, Is.EquivalentTo(resultU));
            Assert.That(yuv444Frame.VBytes, Is.EquivalentTo(resultV));

            Assert.That(yuv444Frame2.YBytes, Is.EquivalentTo(resultY));
            Assert.That(yuv444Frame2.UBytes, Is.EquivalentTo(resultU));
            Assert.That(yuv444Frame2.VBytes, Is.EquivalentTo(resultV));
        }



